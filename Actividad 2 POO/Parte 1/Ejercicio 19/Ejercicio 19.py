
lado = float(input("Ingrese el valor del lado del triángulo equilátero: "))

perimetro = 3 * lado
altura = (lado * (3 ** 0.5)) / 2  
area = (lado ** 2) * (3 ** 0.5) / 4  

print("El perímetro del triángulo es:", perimetro)
print("La altura del triángulo es:", altura)
print("El área del triángulo es:", area)
